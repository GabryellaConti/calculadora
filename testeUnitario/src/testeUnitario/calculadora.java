package testeUnitario;

public class calculadora {
    
	public int somar(int a, int b)       {
    	return a+b;
    	
    }
    public int subtrair(int c,int d) {
    	return c-d;
    }
	public int dividir(int e, int f) {
		return e/f;
	}
	public int multiplicar(int g, int h) {
		return g*h;
	}
	public int perimetro(int b, int a) {
		return b*a;
	}
}
